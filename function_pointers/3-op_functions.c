/**
 * op_add - additionne deux entiers
 * @a: 1er nombre
 * @b: 2e nombre
 *
 * Return: le resultat
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - soustraie deux entiers
 * @a: 1er nombre
 * @b: 2e nombre
 *
 * Return: le resultat
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplie deux entiers
 * @a: 1er nombre
 * @b: 2e nombre
 *
 * Return: le resultat
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divise deux entiers
 * @a: 1er nombre
 * @b: 2e nombre
 *
 * Return: le resultat
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - division avec reste deux entiers
 * @a: 1er nombre
 * @b: 2e nombre
 *
 * Return: le resultat
 */

int op_mod(int a, int b)
{
	return (a % b);
}
